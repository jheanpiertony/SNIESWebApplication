                var _PartEstadoFecha = new PartEstadoFecha
                {
                    Documento = item,
                    FechaExp = (_partpreg != null) ? _partpreg.FECHA_EXPEDICION : (_partposg != null) ? _partposg.FECHA_EXPEDICION : string.Empty,
                    IdEstado = (_partpreg != null) ? _partpreg.ID_ESTADO_CIVIL : (_partposg != null) ? _partposg.ID_ESTADO_CIVIL : string.Empty
                };
                listaPartEstadoFech.Add(_PartEstadoFecha);
            }


            CrearExcelT(listaPartEstadoFech);


            ViewBag.Message = "Your contact page.";

            return View();
        }

        public void CrearExcelT<T>(List<T> lista)
        {
            CrearExcel excel = new CrearExcel();
            DataTable dt = excel.ToDataTable<T>(lista);
            string nombre = dt.TableName.ToString();

            using (XLWorkbook wb = new XLWorkbook())//https://github.com/ClosedXML/ClosedXML <----- la libreria
            {
                wb.Worksheets.Add(dt, nombre);
                Response.Clear();
                Response.Buffer = true;
                Response.Charset = "";
                Response.ContentType = "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet";
                Response.AddHeader("content-disposition", "attachment;filename=" + nombre + ".xlsx");
                using (MemoryStream MyMemoryStream = new MemoryStream())
                {
                    wb.SaveAs(MyMemoryStream);
                    MyMemoryStream.WriteTo(Response.OutputStream);
                    Response.Flush();
                    Response.End();
                }
            }
        }

