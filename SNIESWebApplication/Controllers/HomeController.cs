            }

            foreach (var item in lista)
            {
                var _partposg = saintParticipatePosg.Where(x => x.NUM_DOCUMENTO == item).Select(x => new { x.FECHA_EXPEDICION, x.ID_ESTADO_CIVIL }).FirstOrDefault();
